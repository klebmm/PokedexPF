    private void crearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearActionPerformed
        Usuario user = new Usuario();
        UsuarioJDBC userdb = new UsuarioJDBC();

        String usr = jTextFieldUsuario.getText();
        String pwd = jTextFieldPwd.getText();
        String pwdyes = jTextFieldPwdC.getText();
        String mail = jTextFieldMail.getText();
        if (pwd.equals(pwdyes)){
            String contrase = pwd;
            user.setUsername(usr);
            user.setMail(mail);
            user.setPassword(contrase);
            userdb.insert(user);
            OpenLogin();
        } else {
            JOptionPane.showMessageDialog(jFrame, "Error, colocar las mismas contrase침as");
        }
    }//GEN-LAST:event_crearActionPerformed

